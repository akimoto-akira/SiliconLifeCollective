```

**작업 우선순위**:
- 숫자가 작을수록 우선순위가 높음
- 우선순위 1: 최고 우선순위
- 우선순위 5: 일반 우선순위

**작업 상태 흐름**:
1. **대기 중** (Pending): 작업이 생성되고, 시작 대기
2. **실행 중** (Running): 작업 실행 시작
3. **완료** (Completed): 작업이 성공적으로 완료
4. **실패** (Failed): 작업 실행 실패
5. **취소** (Cancelled): 작업이 취소됨

