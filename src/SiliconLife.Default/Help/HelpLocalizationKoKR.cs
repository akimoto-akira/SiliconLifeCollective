- **운영 환경**: Information 또는 Warning 레벨 사용
- **문제 해결**: 일시적으로 Debug 레벨로 낮춤

### 2. 어떤 로그에 중점을 두어야 하나요?

다음 상황에 중점을 둘 것을 권장합니다:
- 시스템 시작 및 종료 정보
- 실리콘 생명체 생성, 실행 이상 및 중지
- 권한 검증 실패 및 보안 문제
- AI 호출 실패 및 응답 이상
- 도구 실행 오류 및 시간 초과

### 3. 과도한 로그로 인한 성능 영향 방지

로그 파일이 너무 큰 경우 로그 상세도 설정을 높여 (예: ""Warning"" 또는 ""Error""로 변경) 기록량을 줄입니다.

### 4. 민감한 정보 보호

시스템은 다음 민감한 정보를 로그에 기록하지 않습니다:
- 비밀번호 및 키
- 개인 식별 정보
- API 키 및 토큰
- 데이터베이스 연결 문자열

## 자주 묻는 질문

### Q: 왜 일부 로그 메시지가 보이지 않나요?

**A**: 가능한 원인:
1. 로그 상세도 설정이 너무 높아 일부 정보가 필터링됨 (구성에서 ""Debug"" 또는 ""Trace""로 조정 가능)
2. 시스템이 올바르게 시작되지 않아 로그 기능이 초기화되지 않음

### Q: 더 상세한 로그를 보려면?

**A**: 구성 파일의 로그 상세도 설정을 ""Debug"" 또는 ""Trace""로 변경한 후 시스템을 재시작합니다.

### Q: 로그 파일은 어디에 있나요?

**A**: 로그 파일은 `data/Log/` 디렉토리에 저장되며 시간별로 인덱스됩니다.

