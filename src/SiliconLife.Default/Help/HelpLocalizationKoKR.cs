## 모델 다운로드 및 실행.
### 모델이란?
모델은 AI의 ""두뇌""로, AI의 능력을 결정합니다. 다른 모델은 다른 특징을 가지고 있습니다:
- **크기가 다릅니다**: 모델이 클수록 능력이 강하지만 더 많은 메모리가 필요합니다.
- **전문 분야가 다릅니다**: 어떤 모델은 대화에 능숙하고, 어떤 모델은 프로그래밍에 능숙합니다.
### 모델 지능(B 단위).
AI 모델의 ""지능""은 일반적으로 **B(Billion, 십억 매개변수)** 로 표시됩니다:
- **7B-8B**: 기본 레벨, 간단한 작업을 완료할 수 있지만 복잡한 장면에서는 성능이 좋지 않을 수 있습니다.
- **13B-14B**: 중간 레벨, 대부분의 일상 작업에서 성능이 좋습니다.
- **32B 이상**: 높은 레벨, 복잡한 추론, 긴 텍스트 이해 능력이 더 강합니다.
**이 시스템은 8B 이상의 모델을 권장합니다**. 더 나은 사용 경험을 얻기 위해.
### 로컬 모델 vs 클라우드 모델.
Ollama는 두 가지 모델 실행 방식을 지원합니다:
**로컬 모델:**
- 모델 파일이 컴퓨터에 다운로드됩니다.
- 완전히 로컬에서 실행되며 다운로드 후 인터넷에 연결할 필요가 없습니다.
- 하드웨어 구성(메모리, 그래픽 카드)에 제한됩니다.
- 일반적으로 4B-70B 매개변수.
- 무료로 사용하며 제한이 없습니다.
**클라우드 모델:**
- 모델이 Ollama 클라우드 서버에서 실행됩니다.
- 모델 식별자만 다운로드하면 됩니다(매우 작습니다).
- 일반 컴퓨터에서 실행할 수 없는 초대형 모델을 실행할 수 있습니다(일반적으로 200B 이상).
- 사용하려면 인터넷에 연결해야 합니다.
- 사용 한도가 있습니다(**매주 갱신**).
- Ollama 클라이언트의 클라우드 기능을 켜면 사용할 수 있습니다.
