2. endpoint 주소가 올바른지 확인(해당 서비스의 기본 설정 참조)
3. 브라우저에서 서비스 주소에 접속, 응답이 표시되어야 합니다.
4. 모델이 다운로드되었는지 확인: 해당 서비스의 명령어로 모델 목록 보기

### Q5: 클라우드 클라이언트에서 인증 실패가 표시되나요?

**해결 방법:**
1. API 키가 올바른지 확인(형식은 해당 플랫폼의 요구사항 참조)
2. 계정이 연체되지 않았는지 확인
3. 사용량 할당량을 초과하지 않았는지 확인
4. API 키를 다시 생성하고 설정 업데이트

### Q6: 클라우드 클라이언트의 API 키는 어떻게 획득하나요?

**일반 단계:**
1. 해당 AI 서비스 제공업체의 콘솔 사이트 방문
2. 계정 로그인
3. ""키 관리"" 또는 ""API 관리"" 페이지 진입
4. ""키 생성"" 또는 ""API 키 생성"" 클릭
5. 생성된 API 키를 복사하여 안전하게 저장

### Q7: 응답이 매우 느리면 어떻게 하나요?

