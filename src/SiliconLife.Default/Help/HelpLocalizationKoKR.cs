- **BeingId**: 연관된 실리콘 생명체 ID (시스템 로그는 비어 있음)
- **Timestamp**: 로그 생성 시간 (UTC)
- **Level**: 로그 레벨
- **Category**: 로그 카테고리
- **Message**: 로그 메시지
- **Exception**: 예외 정보 (있는 경우)

