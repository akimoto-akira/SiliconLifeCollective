### Q8: temperature 파라미터는 무엇인가요?

temperature는 AI 응답의 창의성을 제어합니다:
- **0.0-0.3**: 매우 보수적, 응답 예측 가능, 사실적 질문에 적합
- **0.4-0.7**: 균형 모드, 일상 대화에 적합(권장)
- **0.8-1.0**: 매우 창의적, 응답 다양, 창의적 글쓰기에 적합

### Q9: maxTokens는 얼마나 설정해야 하나요?

- **1024**: 짧은 응답, 간단한 질문 응답에 적합
- **2048**: 중간 길이, 일반 대화에 적합(권장)
- **4096+**: 긴 응답, 복잡한 작업 또는 긴 텍스트 생성에 적합

참고: 설정이 클수록 소비되는 리소스와 시간이 많아집니다.

