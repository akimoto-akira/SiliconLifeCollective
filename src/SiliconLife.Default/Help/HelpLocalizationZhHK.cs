## 什麼是阿里雲百煉？

阿里雲百煉（DashScope）是阿里雲提供的大型模型服務平台，提供多種高質量的AI模型，包括通義千問、DeepSeek、GLM、Kimi等。

**優點：**
- 模型智商高（最高可達幾百B）
- 無需本地硬體，雲端運行
- 支援多種頂級AI模型
- 按量付費，成本可控
- 相容 OpenAI API 格式

## 註冊和開通服務

### 步驟1：註冊阿里雲帳號

1. 存取阿里雲官網：https://www.aliyun.com
2. 點擊""免費註冊""
3. 按提示完成註冊（支援手機號、信箱註冊）
4. 完成實名認證（需要使用支付寶或銀行卡）

### 步驟2：開通百煉服務

1. 登入阿里雲控制台
2. 搜尋""百煉""或""DashScope""
3. 點擊進入百煉產品頁面
4. 點擊""立即開通""
5. 閱讀並同意服務協議
6. 完成開通

### 步驟3：取得 API Key

1. 進入百煉控制台
2. 在左側選單找到""API Key 管理""或""金鑰管理""
3. 點擊""建立 API Key""
4. 為金鑰命名（如""SiliconLife""）
5. 複製並儲存 API Key（**只顯示一次，請妥善儲存**）

## 在 Silicon Life 中配置百煉

### 配置步驟

1. 開啟 Silicon Life 系統
2. 進入 **⚙ 配置** 頁面
3. 找到""AI客戶端類型""，選擇 `DashScopeClient`
4. 在""AI配置""中填寫：
   - **API Key**：貼上您複製的 API Key
   - **Region（區域）**：選擇伺服器區域（如 beijing）
   - **Model（模型）**：選擇模型（**填寫 API Key 和選擇區域後，系統會自動擷取該區域所有可用模型**）
5. 儲存配置

**提示：**
- 必須先填寫 API Key 並選擇區域，模型下拉清單才會載入
- 如果模型清單載入失敗，會顯示推薦模型清單

