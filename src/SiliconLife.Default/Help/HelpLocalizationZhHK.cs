## 費用說明

### 計費方式

百煉平台採用**按量付費**模式：
- 按輸入的 token 數量計費
- 不同模型價格不同
- 模型智商越高，價格越貴

### 免費額度

- 新使用者通常有免費試用額度
- 部分模型有免費呼叫額度
- 具體額度以百煉平台公告為準

### 查看用量

1. 登入百煉控制台
2. 進入""用量查詢""或""帳單管理""
3. 查看呼叫次數和費用

### 節省費用建議

- 選擇適合的模型（不一定選最貴的）
- 避免傳送過長的文字
- 定期查看用量，控制成本

### Token 用量統計說明

**重要提示：** 由於阿里雲百煉平台（DashScope）的 API 回應中**沒有穩定回傳 Token 用量欄位**，本系統**無法統計**使用百煉模型時的對話 Token 用量。

**這意味著：**
- 系統內不會顯示使用了多少 Token
- 無法在系統內查看歷史 Token 消耗記錄
- 無法根據 Token 用量進行成本分析

**如何查看用量：**
- 請登入阿里雲百煉控制台查看實際用量和費用
- 百煉控制台會提供詳細的呼叫統計和帳單資訊

## 常見問題

### Q: API Key 在哪裡取得？

**A:** 
1. 登入阿里雲百煉控制台
2. 找到""API Key 管理""
3. 建立新的 API Key
4. 複製並妥善儲存

### Q: API Key 外洩了怎麼辦？

**A:**
1. 立即登入百煉控制台
2. 刪除外洩的 API Key
3. 建立新的 API Key
4. 在 Silicon Life 中更新配置

### Q: 選擇哪個區域最好？

**A:**
- **國內使用者**：選擇 beijing（北京），速度最快
- **海外使用者**：選擇離您最近的區域
- 區域不影響模型品質，只影響存取速度

### Q: 為什麼模型清單載入失敗？

**可能原因：**
1. API Key 不正確或已失效
2. 網路連接問題
3. 百煉服務異常

**解決方法：**
1. 檢查 API Key 是否正確
2. 檢查網路連接
3. 稍後重試

### Q: 可以使用多個模型嗎？

**A:** 可以。在 Silicon Life 中，可以為不同的硅基人配置不同的百煉模型。

### Q: 百煉和 Ollama 有什麼區別？

| 特性 | 百煉（DashScope） | Ollama |
|------|------------------|--------|
| 運行位置 | 雲端 | 本地電腦 |
| 硬體要求 | 無 | 需要較高配置 |
| 模型大小 | 可達幾百B | 通常 4B-70B |
| 費用 | 按量付費 | 免費 |
| 聯網 | 必須聯網 | 下載後無需聯網 |
| 私隱 | 資料傳送到雲端 | 完全本地 |

**選擇建議：**
- 硬體配置低 → 選擇百煉
- 需要超高智商模型 → 選擇百煉
- 注重私隱、不想花錢 → 選擇 Ollama
- 斷網環境使用 → 選擇 Ollama

### Q: 呼叫失敗怎麼辦？

**檢查：**
1. API Key 是否正確
2. 帳戶餘額是否充足
3. 是否超出免費額度
4. 查看百煉控制台的錯誤資訊

### Q: 如何控制費用？

**建議：**
1. 設定預算告警（百煉控制台）
2. 定期查看用量
3. 選擇性價比高的模型
4. 避免頻繁呼叫大型模型

## 最佳實踐

### 1. 選擇合適的模型

- **日常對話**：qwen3.6-plus（平衡）
- **複雜推理**：qwen3-max 或 deepseek-r1
- **程式設計任務**：qwen3-coder-plus
