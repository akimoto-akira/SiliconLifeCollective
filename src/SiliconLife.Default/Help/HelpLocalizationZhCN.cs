- **擅长领域不同**：有的擅长对话，有的擅长编程

### 模型智商（B单位）

AI模型的""智商""通常用 **B（Billion，十亿参数）** 来表示：
- **7B-8B**：基础水平，可以完成简单任务，但复杂场景可能表现不佳
- **13B-14B**：中等水平，大多数日常任务表现良好
- **32B及以上**：较高水平，复杂推理、长文本理解能力更强

**本系统推荐使用 8B 以上的模型**，以获得较好的使用体验。

### 本地模型 vs 云端模型

Ollama 支持两种模型运行方式：

**本地模型：**
- 模型文件下载到您的电脑
- 完全本地运行，无需联网（下载后）
- 受限于您的硬件配置（内存、显卡）
- 通常 4B-70B 参数
- 免费使用，无限制

**云端模型：**
- 模型运行在 Ollama 云端服务器
- 只需要下载模型标识符（很小）
- 可以运行家庭电脑无法承载的超大型模型（通常 200B 以上）
- 需要联网使用
- 有使用额度限制（**每周刷新**）
- 开启 Ollama 客户端的云端功能即可使用

