- 通常 4B-70B 参数
- 免费使用，无限制

**云端模型：**
- 模型运行在 Ollama 云端服务器
- 只需要下载模型标识符（很小）
- 可以运行家庭电脑无法承载的超大型模型（通常 200B 以上）
- 需要联网使用
- 有使用额度限制（**每周刷新**）
- 开启 Ollama 客户端的云端功能即可使用

