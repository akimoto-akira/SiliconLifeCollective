记忆系统记录了硅基人的所有活动历史，包括对话、工具调用、系统事件等。通过记忆系统，您可以了解硅基人做过什么、什么时候做的、结果如何。

## 如何访问记忆系统？

通过硅基人页面进入：

1. 点击左侧 **🧠 硅基人** 图标
2. 点击要查看的硅基人卡片
3. 在右侧详情中找到""记忆""链接
4. 点击进入记忆页面

## 记忆页面说明

### 页面布局

- **顶部**：硅基人选择器和统计信息
- **筛选区**：类型、时间、关键词等筛选条件
- **列表区**：显示记忆条目列表
- **详情区**：点击某条记忆后显示详细内容

### 记忆类型

系统会记录以下类型的记忆：

- **对话**：用户和AI的对话内容
- **工具调用**：AI调用工具的执行记录
- **系统事件**：系统运行的重要事件
- **摘要**：对话或事件的压缩摘要

## 查看记忆

### 浏览记忆列表

1. 选择要查看的硅基人
2. 页面会显示该硅基人的记忆列表
3. 每条记忆显示：
   - 类型图标
   - 内容摘要
   - 时间
   - 状态（成功/失败）

### 查看记忆详情

点击任意记忆条目，会显示：
- 完整内容
- 时间戳
- 相关参数
- 执行结果（如果是工具调用）

### 追溯原始上下文

对于某些记忆条目，系统提供""追溯""功能：
1. 点击记忆详情中的""追溯""按钮
2. 系统会显示该记忆发生时的完整上下文
3. 帮助您理解AI当时为什么这么做

## 筛选记忆

### 按类型筛选

点击类型筛选器，选择要查看的记忆类型：
- 只看对话
- 只看工具调用
- 只看系统事件
- 只看摘要

### 按时间筛选

可以选择时间范围：
- 输入开始日期
- 输入结束日期
- 只显示该时间段内的记忆

### 关键词搜索

在搜索框中输入关键词：
- 支持中文和英文
- 会搜索记忆的全部内容
- 输入后自动显示匹配结果

**搜索技巧：**
- 使用具体的关键词更容易找到结果
- 可以结合类型和时间筛选
- 如果结果太多，尝试更具体的关键词

### 显示摘要或原始记录

- **全部显示**：显示所有记忆
- **仅摘要**：只显示压缩后的摘要记录
- **仅原始**：只显示原始详细记录

## 记忆统计

页面顶部会显示统计信息：
- 记忆总数
- 各类型记忆的数量
- 存储占用情况

通过这些统计，您可以了解：
- 硅基人活跃程度
- 主要进行的活动类型
- 是否需要清理旧记忆

## 分页浏览

如果记忆很多，系统会分页显示：
- 每页默认显示20条记录
- 使用页码按钮翻页
- 可以调整每页显示数量

## 常见问题

### Q: 如何找到特定的对话？

**方法：**
1. 在搜索框中输入对话中的关键词
2. 类型筛选选择""对话""
3. 如果知道大概时间，可以设置时间范围
4. 浏览搜索结果

### Q: 记忆占用空间太大怎么办？

**建议：**
- 记忆是自动管理的，通常不需要手动干预
- 系统会创建摘要来压缩历史记录
- 如果确实需要，可以联系系统管理员

### Q: 可以删除记忆吗？

**A:** 系统不提供删除记忆的功能。记忆是硅基人的重要历史，保留记忆有助于AI更好地理解和回答问题。

### Q: 可以导出记忆吗？

**A:** 当前版本不支持导出功能。记忆数据存储在系统数据目录中。

### Q: 为什么有些记忆是""摘要""？

**A:** 系统会自动将较长的对话或事件压缩成摘要，以节省存储空间并提高查询效率。摘要保留了关键信息，但省略了细节。

### Q: 如何查看AI调用工具的详细信息？

**方法：**
1. 类型筛选选择""工具调用""
2. 找到对应的工具调用记录
3. 点击查看详细信息
4. 可以看到工具名称、参数、执行结果等

### Q: 记忆搜索不到结果？

**建议：**
1. 检查关键词是否正确
2. 尝试使用不同的关键词
3. 检查时间范围是否设置正确
4. 确认选择的硅基人是否正确
5. 尝试不设置筛选条件，查看全部记忆

## 使用建议

1. **定期查看**：了解硅基人的活动情况
2. **善用筛选**：快速定位需要的信息
3. **使用追溯**：理解AI的决策过程
4. **关注统计**：了解系统运行状态

## 技术说明

### 数据存储

记忆数据存储在系统数据目录中：
```
