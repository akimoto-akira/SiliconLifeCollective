**方法二：特定のシリコン生命体のタイマーを表示**

1. **🧠 シリコン生命体** ページに入る
2. 確認したいシリコン生命体をクリック
3. 詳細でタイマーリンクを見つける
4. クリックしてタイマーページに入る

### タイマー情報

各タイマーは以下の情報を表示します：

- **タイマー名**：タイマーの識別子
- **ステータス**：実行中または停止中
- **タイプ**：タイマーのトリガータイプ
- **トリガー時間**：次回のトリガー時間
- **カレンダーシステム**：使用されているカレンダー（グレゴリオ暦、旧暦など）
- **トリガー回数**：トリガーされた総回数
- **作成時間**：タイマーが作成された時間
- **最終トリガー時間**：前回のトリガー時間

### タイマータイプ

システムは複数のトリガー方法に対応しています：

- **間隔トリガー**：固定時間ごとに1回トリガー
  - 例：2時間ごと、30分ごと
  
- **カレンダーリガー**：カレンダー条件に基づいてトリガー
  - 例：毎日9時、毎週月曜日、毎月1日
