# シリコン生命体管理

## シリコン生命体とは？

シリコン生命体（Silicon Being）はシステムのコアエンティティで、各シリコン生命体は独立したAIエージェントであり、以下を持っています：
- **ソウルファイル**：コアプロンプト、行動パターン、性格、能力を定義
- **メモリシステム**：対話履歴と重要情報を保存
- **タスクシステム**：定时タスクと自動化操作を実行
- **ツールセット**：呼び出し可能な各種機能ツール

