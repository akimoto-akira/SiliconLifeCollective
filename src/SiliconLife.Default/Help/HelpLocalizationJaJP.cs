    public override string FAQ => @"
# よくある質問

## システム関連

### Q: システムが起動しない場合はどうすればよいですか？

A: 以下を確認してください：
1. ポート8080がすでに使用されていないか
2. .NET 9 ランタイムが正しくインストールされているか
3. ログファイルで詳細なエラーメッセージを確認

### Q: リッスンポートを変更するには？

A: 設定ファイルで `WebHost:Port` を変更するか、コマンドライン引数を使用します。

### Q: データはどこに保存されますか？

A: デフォルトでは、シリコンビーイングのルートディレクトリ下の `data` フォルダに保存されます。

## AI関連

### Q: AIの応答が遅い場合はどうすればよいですか？

A: 考えられる原因：
1. モデルが大きく、より多くの計算リソースが必要
2. ネットワーク遅延（クラウドモデル使用時）
3. ローカルモデル（Ollamaなど）の使用を検討

### Q: AIモデルを切り替えるには？

A: 設定ファイルで `AI:DefaultProvider` を変更するか、ビーイング設定で異なるモデルを選択します。

### Q: AIがツールを呼び出せない場合は？

A: 確認事項：
1. ツールが正しく登録されているか
2. 権限がツール呼び出しを許可しているか
3. AIモデルがツール呼び出し機能をサポートしているか

## ビーイング関連

### Q: ビーイングのデータをバックアップするには？

A: ビーイングディレクトリ下のすべてのファイルをバックアップ場所にコピーします。

### Q: ソウルファイルをインポート/エクスポートできますか？

A: はい。ビーイング編集ページでMarkdown形式のソウルファイルをインポート/エクスポートできます。

### Q: ビーイングをクローンするには？

A: ビーイングリストで「クローン」を選択すると、システムがコピーを作成し、設定を変更できます。

## ヘルプの取得

上記で問題が解決しない場合：

1. [プロジェクトドキュメント](https://github.com/your-repo/docs)を確認
2. Issueを提出して問題を報告
3. コミュニティディスカッションに参加
";
    
