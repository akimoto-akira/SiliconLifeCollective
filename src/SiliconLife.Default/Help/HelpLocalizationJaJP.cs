タスクには5つの状態があります:

| 状態 | 説明 |
|------|------|
| **待機中** | タスクが作成され、実行開始を待機中 |
| **実行中** | タスクが実行中 |
| **完了** | タスクが正常に完了 |
| **失敗** | タスクの実行に失敗（エラー情報を確認可能） |
| **取消済** | タスクが取消され、これ以上実行されない |

