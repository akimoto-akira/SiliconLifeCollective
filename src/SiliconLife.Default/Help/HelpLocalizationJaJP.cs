## 費用の説明。
### 課金方法。
百錬プラットフォームは**従量課金**モードを採用：
- 入力された token 数量で課金。
- モデルによって価格が異なる。
- モデルの知能が高いほど、価格が高い。
### 無料枠。
- 新規ユーザーは通常無料試用枠がある。
- 一部のモデルには無料呼び出し枠がある。
- 具体的な枠は百錬プラットフォームの公告に基づく。
### 使用量の確認。
1. 百錬コンソールにログイン。
2. 使用量照会または請求管理に入る。
3. 呼び出し回数と費用を確認。
### 費用節約のアドバイス。
- 適したモデルを選択（最も高いものを選ぶ必要はない）。
- 長すぎるテキストの送信を避ける。
- 定期的に使用量を確認して、コストを制御。
### Token 使用量統計の説明。
**重要なお知らせ：** アリババクラウド百錬プラットフォーム（DashScope）の API レスポンスには**Token 使用量フィールドが安定して返されない**ため、本システムは百錬モデル使用時の対話 Token 使用量を**統計できません**。
**これは：**
- システム内に使用した Token 数が表示されない。
- システム内で過去の Token 消費記録を確認できない。
- Token 使用量に基づいてコスト分析できない。
**使用量を確認する方法：**
- アリババクラウド百錬コンソールにログインして実際の使用量と費用を確認。
- 百錬コンソールは詳細な呼び出し統計と請求情報を提供。
