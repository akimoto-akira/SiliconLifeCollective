- **自動チェック**: システムが自動的に依存関係をチェックし、依存するすべてのタスクが完了してからでないと、現在のタスクは開始できません
- **循環依存の検出**: システムが循環依存（例：AがBに依存、BがAに依存）を検出して阻止します

### タスクの依存関係を確認

