## よくある質問

### Q: ソウルファイルを修正した後、すぐに有効になりますか？

**A:** はい、保存後すぐに有効になります。次にシリコンビーイングが応答するとき、新しいソウルファイルを使用します。

### Q: ソウルファイルにサイズ制限はありますか？

**A:** 厳格なサイズ制限はありませんが、合理的な範囲内（数千語以内）に抑えることを推奨します。長すぎるソウルファイルは応答速度に影響を与える可能性があります。

### Q: ソウルファイルを完全に削除できますか？

**A:** ソウルファイルの削除は推奨しません。内容が空の場合、シリコンビーイングは行動ガイダンスを失い、予期しない応答を生成する可能性があります。

### Q: ソウルファイルをバックアップするにはどうすればよいですか？

**A:** 重要なシリコンビーイングのソウルファイルを定期的にバックアップすることを推奨します。以下の方法があります：
1. Web インターフェースを介して内容をローカルファイルにコピーする
2. システムのエクスポート機能を使用する（サポートされている場合）
3. データディレクトリを直接バックアップする

### Q: ソウルファイルとメモリシステムの関係は何ですか？

**A:** ソウルファイルは**長期的な行動パターン**を定義し、メモリシステムは**短期的な会話履歴**を記録します。両者は協力して動作します：
- ソウルファイル：シリコンビーイングに「あなたがどのようなロールか」を伝える
- メモリシステム：シリコンビーイングに「以前何を話したか」を伝える

### Q: 異なるシリコンビーイングが同じソウルファイルを使用できますか？

**A:** はい、ただし推奨しません。各シリコンビーイングはユニークなロールポジショニングを持つべきであり、機能の重複を避けるべきです。

